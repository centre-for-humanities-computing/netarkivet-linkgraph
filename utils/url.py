NA = Literal[np.nan]

def parse_domain(url: str) -> Union[str, NA]:
    """
    Normalizes urls to all have the same form.

    Parameters
    ----------
    url: str
        Raw url

    Returns
    -------
    domain: str or NA
        Domain name.
        Returns np.nan if the url is invalid
        or does not contain a domain name.
    """
    try:
        domain = urlparse(url).netloc
    except ValueError:
        #print(f"invalid: {url}")
        return np.nan
    return domain or np.nan

def normalize_domains(domains: pd.Series) -> pd.Series:
    return domains.where(
        domains.str.startswith("www."),
        "www." + domains
    )

def strip_braces(urls: pd.Series) -> pd.Series:
    return (
        urls
        .str.lower()
        .str.replace(
            r"\[.*\]", "", regex=True
        )
        .str.replace(
            r"\].*", "", regex=True
        )
    )

